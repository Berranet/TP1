public class Amortissement {
    /**
     * Variable de type integer pour définir la periode 
     */
    public int periode;
    /**
     * Variable de type bouble pour définir le capital du début 
     */
    public double capitalDebut;
    /**
     * Variable de type bouble pour définir le versement total; 
     */
    public double versementTotal;
    /**
     * Variable de type bouble pour définir le versement d'intéret 
     */
    public double versementInteret;
    /**
     * Variable de type bouble pour définir le versement du capital 
     */
    public double versementCapital;
    /**
     * Variable de type bouble pour définir le capital de la fin 
     */
    public double capitalFin;
    /**
     * Variable de type bouble pour définir le versement total cumulatif
     */
    public double versementTotalCumulatif;
    /**
     * Variable de type bouble pour définir le versement d'interet cumulatif
     */
    public double versementInteretCumulatif;
    /**
     * Variable de type bouble pour définir le versement capital cumulatif
     */
    public double versementCapitalCumulatif;
}
